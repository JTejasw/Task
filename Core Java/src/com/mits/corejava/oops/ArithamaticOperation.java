//class open
public class ArithamaticOperation {
	// add method
	public int add(int num1, int num2)
	{
		int add = num1 + num2;
		return add;
	}
	// sub method
	public int sub(int num1, int num2)
	{
		int sub = num1 - num2;
		return sub;
	}
	// mul method
	public int mul(int num1, int num2)
	{
		int mul = num1 * num2;
		return mul;
	}
	// div method
	public int div(int num1, int num2)
	{
		int div = num1 / num2;
		return div;
	}
}// end class
